// Функция для создания комментария
const createComment = () => {
  // Случайное количество предложений (1 или 2)
  const sentenceCount = getRandomInteger(1, 2);
  let message = '';

  // Выбираем случайные предложения
  const shuffledMessages = [...MESSAGES].sort(() => Math.random() - 0.5);

  for (let i = 0; i < sentenceCount; i++) {
    if (i > 0) {
      message += ' ';
    }
    message += shuffledMessages[i];
  }

  return {
    id: generateCommentId(),
    avatar: `img/avatar-${getRandomInteger(1, 6)}.svg`,
    message: message,
    name: getRandomElement(NAMES)
  };
};

// Функция для создания массива комментариев
const createComments = () => {
  const commentsCount = getRandomInteger(0, 30);
  const comments = [];

  for (let i = 0; i < commentsCount; i++) {
    comments.push(createComment());
  }

  return comments;
};

// Функция для создания объекта фотографии
const createPhoto = (id) => ({
  id: id,
  url: `photos/${id}.jpg`,
  description: DESCRIPTIONS[id - 1] || `Фотография ${id}`,
  likes: getRandomInteger(15, 200),
  comments: createComments()
});

// Функция для создания массива из 25 фотографий
const createPhotosArray = () => {
  const photos = [];

  for (let i = 1; i <= 25; i++) {
    photos.push(createPhoto(i));
  }

  return photos;
};

